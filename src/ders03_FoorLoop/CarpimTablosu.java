package ders03_FoorLoop;

public class CarpimTablosu {

    // carpim tablosu yapiniz
    public static void main(String[] args) {

        for (int i = 0; i <=10; i++) {
            System.out.println("*****************");

            for (int j = 0; j <=10 ; j++) {

                System.out.println(i + " * " + j + " = " + (i*j));

            }

        }




    }
}
